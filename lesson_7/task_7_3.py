# –∑–∞–¥–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π cl, –∑–Ω–∞—á–µ–Ω–∏–µ —Å–∏–º–≤–æ–ª–∞ –∫–ª–µ—Ç–∫–∏
cl = 'ü§ê'

# —Å–æ–∑–¥–∞–µ–º –∫–ª–∞—Å—Å –ö–ª–µ—Ç–∫–∞.
class Cell:
    def __init__(self, quantity):
        self.quantity = quantity

    def __str__(self):
        return f'{self.quantity}'

    # –†–µ–∞–ª–∏–∑—É–µ–º –º–µ—Ç–æ–¥ –ø–µ—Ä–µ–≥—Ä—É–∑–∫–∏ —Å–ª–æ–∂–µ–Ω–∏–µ (__add__())
    def __add__(self, other):
        print(f'\n–°–ª–æ–∂–µ–Ω–∏–µ –∫–ª–µ—Ç–æ–∫')
        return Cell(self.quantity + other.quantity)

    # –†–µ–∞–ª–∏–∑—É–µ–º –º–µ—Ç–æ–¥ –ø–µ—Ä–µ–≥—Ä—É–∑–∫–∏ –≤—ã—á–∏—Ç–∞–Ω–∏–µ (__sub__())
    def __sub__(self, other):
        print(f'\n–í—ã—á–∏—Ç–∞–Ω–∏–µ –∫–ª–µ—Ç–æ–∫')
        return Cell(self.quantity - other.quantity) if (self.quantity - other.quantity) > 0\
            else print('–í—ã—á–∏—Ç–∞–Ω–∏–µ –∫–ª–µ—Ç–æ–∫ –Ω–µ–≤–æ–∑–º–æ–∂–Ω–æ!')

    # –†–µ–∞–ª–∏–∑—É–µ–º –º–µ—Ç–æ–¥ –ø–µ—Ä–µ–≥—Ä—É–∑–∫–∏ —É–º–Ω–æ–∂–µ–Ω–∏–µ (__mul__())
